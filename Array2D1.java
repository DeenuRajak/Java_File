public class Array2D1 {
    public static void main(String []args){
            int nums [][]={ {3,4},{1,2},{4,2}
            };

            for(int i=0;i<3;i++){
                for(int j=0;j<2;j++){
                    System.out.println(nums[i][j]+" ");
                }
                System.out.println();
            }
    }
}